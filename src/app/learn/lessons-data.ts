export interface CodeExample {
  title: string;
  description: string;
  code: string;
  output?: string;
  explanation: string;
  tips?: string[];
}

export interface Exercise {
  id: string;
  title: string;
  description: string;
  difficulty: 'سهل جداً' | 'سهل' | 'متوسط' | 'صعب' | 'صعب جداً';
  hints: string[];
  solution: string;
  solutionExplanation: string;
  learningPoints: string[];
}

export interface Lesson {
  id: string;
  title: string;
  description: string;
  duration: string;
  prerequisites: string[];
  objectives: string[];
  introduction: string;
  sections: {
    title: string;
    content: string;
    keyPoints: string[];
  }[];
  codeExamples: CodeExample[];
  exercises: Exercise[];
  summary: string;
  nextSteps: string[];
}

export interface Level {
  id: string;
  name: string;
  description: string;
  longDescription: string;
  icon: string;
  color: string;
  lessonsCount: number;
  totalHours: string;
  lessons: Lesson[];
}

